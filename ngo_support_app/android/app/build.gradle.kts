        isCoreLibraryDesugaringEnabled = true
    }

    kotlinOptions {
        jvmTarget = JavaVersion.VERSION_11.toString()
    }

    defaultConfig {
        applicationId = "com.beaconnewbeginnings.ngo_support_app"
        minSdk = 23  // Required for security features and encryption
        targetSdk = 34  // Latest Android API
        versionCode = flutter.versionCode
        versionName = flutter.versionName
        
        // Required for store submission
        multiDexEnabled = true
        
        // App metadata for Play Store
        resValue("string", "app_name", "Beacon of New Beginnings")
        resValue("string", "app_description", "Support for survivors of abuse and homelessness")
    }

    signingConfigs {
        create("release") {
            if (keystoreProperties.containsKey("keyAlias")) {
                keyAlias = keystoreProperties["keyAlias"] as String
                keyPassword = keystoreProperties["keyPassword"] as String
                storeFile = file(keystoreProperties["storeFile"] as String)
                storePassword = keystoreProperties["storePassword"] as String
            }
        }
    }

    buildTypes {
        release {
            signingConfig = if (keystoreProperties.containsKey("keyAlias")) {
                signingConfigs.getByName("release")
            } else {
                signingConfigs.getByName("debug")
            }
            isMinifyEnabled = false
            isShrinkResources = false
        }
    }
